//Haz un método que imprima "hola mundo"
package function;

public class Imprimir {
    public static void main(String[] args) {
        holaMundo(); //Llamada del método
    }
    //Realmente esto no es una función, es un método, porque no devulve un valor
    public static void holaMundo(){ //Dentro del parentesis hay parámetros
        System.out.println("Hola mundo");
    }
}
